// Task 1: Program to add two numbers

// Declare and initialize two numbers
let num1 = 10;
let num2 = 20;

// Add the two numbers and store the result in a variable
let sum = num1 + num2;

// Print the result to the console
console.log(`The sum of ${num1} and ${num2} is: ${sum}`);

// Task 2: Program to subtract two numbers

// Declare and initialize two numbers
let num3 = 34;
let num4 = 23;

// Subtract the two numbers and store the result in a variable
let difference = num3 - num4;

// Print the result to the console
console.log(`The difference between ${num3} and ${num4} is: ${difference}`);

// Task 3: Program to multiply two numbers

// Declare and initialize two numbers
let num5 = 40;
let num6 = 54;

// Multiply the two numbers and store the result in a variable
let product = num5 * num6;

// Print the result to the console
console.log(`The product of ${num5} and ${num6} is: ${product}`);

// Task 4: Program to divide two numbers

// Declare and initialize two numbers
let num7 = 66;
let num8 = 11;

// Divide the two numbers and store the result in a variable
let division = num7 / num8;

// Print the result to the console
console.log(`The division of ${num7} by ${num8} is: ${division}`);

// Task 5: Program to find the remainder

// Declare and initialize two numbers
let num9 = 68;
let num10 = 7;

// Find the remainder and store the result in a variable
let rem = num9 % num10;

// Print the result to the console
console.log(`The remainder of ${num9} divided by ${num10} is: ${rem}`);

// Activity 2: Assignment Operators

// Task 6: Using += operator

// Update the value of num1 by adding 4 to it
num1 += 4;

// Print the updated value of num1 to the console
console.log(`The updated value of num1 after using += operator is: ${num1}`);

// Task 7: Using -= operator

// Update the value of num2 by subtracting 5 from it
num2 -= 5;

// Print the updated value of num2 to the console
console.log(`The updated value of num2 after using -= operator is: ${num2}`);

// Activity 3: Comparison Operators

// Task 8: Using comparison operators

// Declare and initialize two numbers for comparison
let a = 10;
let b = 20;

// Comparison examples

// Check if a is equal to b
console.log(`${a} == ${b}:`, a == b);

// Check if a is not equal to b
console.log(`${a} != ${b}:`, a != b);

// Check if a is less than b
console.log(`${a} < ${b}:`, a < b);

// Check if a is greater than b
console.log(`${a} > ${b}:`, a > b);

// Check if a is less than or equal to b
console.log(`${a} <= ${b}:`, a <= b);

// Check if a is greater than or equal to b
console.log(`${a} >= ${b}:`, a >= b);

// Activity 4: Logical and Ternary Operators

// Task 9: Using logical operators

// Declare and initialize boolean values
let x = true;
let y = false;

// Logical AND (&&)
console.log(`x && y: ${x && y}`); // false

// Logical OR (||)
console.log(`x || y: ${x || y}`); // true

// Logical NOT (!)
console.log(`!x: ${!x}`); // false

// Task 10: Using ternary operator

// Declare and initialize a variable
let age = 18;

// Use the ternary operator to check if the person is eligible to vote
let eligibility = (age >= 18) ? "Eligible to vote" : "Not eligible to vote";

// Print the result to the console
console.log(`Age ${age}: ${eligibility}`);
